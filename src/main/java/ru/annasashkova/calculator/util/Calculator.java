package ru.annasashkova.calculator.util;

import java.util.Scanner;

public class Calculator {
    public static int sum(int x, int y) {
        int res = x + y;
        return res;
    }

    public static int multiply(int x, int y) {
        int res = x * y;
        return res;
    }

    public static int minus(int x, int y) {
        int res = x - y;
        return res;
    }
}
